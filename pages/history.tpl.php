    <h3>Ceglédi Judo klub 35 éves</h3>
    <p>A ceglédi judo sport alig több mint 30 éves múltra tekinthet vissza. A 
        CVSE Judo Szakosztálya 1976-ban alakult, 1980-tól kapcsolódtunk be az 
        országos versenyrendszerbe. Szakmailag megalapozott versenyzőképzés 1990-től 
        folyik két edzővel. Az elmúlt két évtizedben több mint 100 magyar bajnoki 
        címet nyertek versenyzőink. Saját nevelésű sportolóink nemzetközi 
        eredményekre is képesek. Korábban Bíró Norbert a látássérültek között 
        nyert Európa Bajnokságot, érmeket hozott az EB-ről és Világ Kupáról, 
        valamint 5. helyezést ért el a Paralimpián. Ungvári Miklós Junior Európa 
        Bajnokságon 5. volt. Vasutas Európa Bajnokságon Ungvári Miklós aranyérmet, 
        Varga István bronzérmet nyert.</p>
    <p>A 2002. év kiemelkedő sikereket hozott. A Mariborban (Szlovénia) megrendezett 
        felnőtt Európa Bajnokságon a szakosztályunkban nevelkedett Ungvári Miklós a 
        férfi 66 kg-ban Európa Bajnok lett. A Jeju-ban (Korea) megrendezett Junior 
        Világbajnokságon Szabó Brigitta az 57 kg-ban aranyérmet és így világbajnoki 
        címet szerzett. Brigitta sporttörténelmet írt, mert ő a sportág első hazai 
        junior világbajnoka. Nem sokkal később a Rotterdamban (Hollandia) megrendezett 
        Junior Európa Bajnokságon is aranyérmes, megszerezve ezzel az Európa Bajnoki 
        címet is. A Magyar Judo Szövetség a 2002. évi eredmények alapján az év női 
        versenyzőjének Szabó Brigittát, az év férfi versenyzőjének Ungvári Miklóst, 
        míg edzőjüket, Bíró Tamást az év edzőjének választotta.</p>
    <p>A 2003. évben elért eredményeink közül kiemelkedik Bíró Norbert a Látássérültek 
        Világbajnokságán elért 5. helye. Ezzel kvalifikálta magát a 2004.évi Athéni 
        Paralimpiai Játékokra. Mester Dániel az Ifjúsági Európa Bajnokságon szintén 
        5. helyet szerzett. Szabó Brigitta képviselte a magyar színeket a düsseldorfi 
        Európa Bajnokságon és az oszakai világbajnokságon. Szabó Brigitta, Ungvári 
        Miklós és Bíró Norbert tehetségüknél, addig elért eredményeiknél fogva 
        olimpiai reménységeink voltak, Hármuk közül Bíró Norbert váltotta valóra 
        olimpiai álmainkat. A XII. Nyári Paralimpiai Játékok során a 60 kg-os 
        súlycsoportban bronzérmet nyert, ezzel a ceglédi judo sport eddigi legnagyobb 
        sikerét érte el. Szabó Brigitta a Moszkvában megrendezett Egyetemi-Főiskolai 
        Világbajnokságon az 5. helyet szerezte meg.</p>
    <p>Bíró Norbert a Látássérültek 2005. évi Európa Bajnokságán, Rotterdamban, 
        ezüstérmet szerzett. A szakosztályunkban nevelkedett Ungvári Miklós a 2005. 
        évi Felnőtt Világbajnokságon, Kairóban, bronzérmes lett. Ezt a teljesítményét 
        a 2007. évben a Rio de Janeiro-i Világbajnokságon megismételte. Ungvári 
        Attila a 2007. évi Junior EB-n Prágában 5. helyezett, az U23 Európa Bajnokságon 
        pedig 7. helyezett volt. Ungvári Miklós 2009-ben Európa Bajnoki címet 
        nyert Tbilisziben, a Rotterdami Világbajnokságon pedig bronzérmes lett.</p>
    <p>Ungvári Attila az Európa Bajnokságon 5., a Világbajnokságon 7. helyezett volt. 
        A Belgrádi Universiade-n ezüstérmet szerzett, ugyancsak 2. az U 23 EB-n 
        Antalya-ban. Az Ungvári fivérek érmeket hoztak Világkupa – Grand Slam és 
        Grand Prix – versenyekről, mindketten tagjai voltak a 2009-ben csapat 
        Európa Bajnoki címet nyert magyar válogatottnak. A 2010. évi Bécsi Európa 
        Bajnokságon Ungvári Attila bronzérmes, Ungvári Miklós ezüstérmes. A Tokiói 
        VB-n Ungvári Miklós 7., Ungvári Attila 9. helyezett. A Budapesti Masters 
        Világbajnokságon Bíró Tamás M-3 73 kg-ban világbajnoki címet nyert, ugyanitt 
        Bíró Norbert az M-2 60 kg-ban 7. helyezett. Ungvári Attila Európa Bajnoki 
        címet nyert az U 23 EB-n Sarajevoban. Ungvári Miklós a 2012-es Londoni 
        Olimpián súlycsoportjában (66 kg) ezüstérmet szerez.</p>
