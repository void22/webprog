    <h3>Ungvári Miklós ezüstérmes</h3>
    <p>A 66 kg-os Ungvári Miklós ezüstérmet szerzett vasárnap a londoni olimpia 
        cselgáncstornáján.</p>
    <img src="./img/ung01.jpg">
    <p>Az első két meccsét a várakozásoknak megfelelően simán hozta, a legjobb nyolc 
        közé jutásért azonban már keményebb feladat várt rá: a szlovén Rok Draksic 
        személyében. Nagy csatában őt is sikerült legyőzni, majd a negyeddöntőben 
        a lengyel Zagrodnik sem tudta Bíró Tamás tanítványát feltartóztatni, így 
        Ungi bejutott a legjobb négy közé! Tizenhat évvel ezelőtt, az 1996-os 
        atlantai olimpián vívhatott legutóbb elődöntőt magyar judós... Az első 
        összecsapásra az afgán Faizzada ellen került sor. Az ellenfél – aki a 
        Nemzetközi Olimpiai Bizottság rendkívüli szabadkártyájával került a 
        mezőnybe – láthatóan borzasztóan örült annak, hogy szerepelhet a londoni 
        olimpián, még akkor is telefonát szurkolóival, amikor a beszólításra 
        várakozott Ungvári oldalán. A tatamin aztán csak tizenhét másodperc jutott 
        neki, a Tatabánya SC-Ippon Judo 66 kilós klasszisa ipponnal jutott be a 
        legjobb harminckettő közé. A második meccsre sem kellett soká várni. 
        A belize-i Sanchez már nem telefonálgatott, az övét is tisztességesebben 
        megkötötte, mint az afgán versenyző, ennek ellenére kevesebb ideig húzta, 
        őt tizenöt másodperc alatt intézte el Ungvári Miklós.</p>
    <img class="img-right" src="./img/ung02.jpg">
    <p>A legjobb nyolc közé jutásért egy igen kellemetlen ellenfél következett: 
        a világbajnoki ötödik helyezett, kétszeres Európa-bajnoki bronzérmes szlovén 
        Rok Draksic, aki legutóbbi két összecsapását megnyerte a magyar judós ellen, 
        2012-ben a tuniszi Grand Prix döntőjében, illetve az idén áprilisban, a 
        cseljabinszki Eb bronzmeccsén kerekedett felül. Elérkezett az idő a 
        visszavágásra! Ezt Ungi is így gondolta! Felvette Draksic harcos stílusát, 
        és ráerőltette az akaratát. Előbb a sok "maszatolás" miatt mindkettejüket 
        intették, majd passzivitás miatt Draksic kapott büntetést. Ez a jukónyi 
        előny ki is tartott a végéig, bár közben több ígéretes indítást is láthattunk 
        Ungváritól. A rutinos magyar judós a sorsdöntő győzelem után sem állt meg 
        nyilatkozni, leszegett fejjel ment tovább az útjára. Amelyet a negyeddöntőben 
        a lengyel Pawel Zagrodnik keresztezett. Az U23-as Európa-bajnoki ezüstérmes 
        ellenfél váratlanul került az olimpiai mezőnybe, hiszen a lengyelek első 
        számú 66 kilósa, Tomasz Kowalski súlyos motorbalesetet szenvedett. 
        Zagrodnik is kvalifikációs helyen állt a ranglistán, így beugorhatott 
        sérült honfitársa helyett. Ha már beugrott, alaposan fel is forgatta az 
        ágát: előbb nagy meglepetésre kiütötte a kétszeres vb-ezüstérmes brazil 
        Cunhát, majd aranypontos meccsen egy intéssel felülmúlta a korábbi 
        Európa-bajnok örmény Nazarjant is. Ungvári Miklós korábban három világversenyen 
        is találkozott Zagrodnikkal, a 2009-es rotterdami világbajnokságon, illetve 
        a tavalyi isztambuli és az idei cseljabinszki Eb-n is a magyar csillag nyert. 
        A londomi judócsoda folytatódik: a spanyol Uriarte sem tudott ellenállni, 
        Ungvári Miklós így bejutott a döntőbe, ahol a a grúz Lasa Savdatuasvilivel 
        mérkőzött. A háromszoros vb-bronzérmes és Európa-bajnok magyar versenyző a 
        fináléban a nála 11 évvel fiatalabb, junior Eb-első grúz Lasa Savdatuasvili 
        ellen lépett tatamira, aki már a felnőttek között is letette névjegyét idén 
        egy Eb-bronzzal. Ungvári afgán, belizei, szlovén, lengyel és spanyol, míg 
        riválisa chilei, francia, brit és japán dzsúdóson keresztül jutott a döntőbe. 
        Savdatuasvili az egész nap folyamán rendkívül agresszívan küzdött, a 
        világbajnok japán Ebinuma Maszasit ipponnal verte az elődöntőben.</p>
    <img src="./img/ung03.jpg">
    <p>A rutin és a fiatalság összecsapására telt ház, 8000 néző volt kíváncsi az 
        ExCeL központ cselgáncstermében. Savdatuasvili rendkívül vehemensen, szinte 
        vadul támadott, egy perc múlva pedig jukót érő levitellel megszerezte a 
        vezetést. Ungvári a földön megpróbálta megkontrázni, de nem sikerült. 
        Két perc elteltével a japán vezetőbíró intette a magyar versenyzőt. Ungvári 
        innentől folyamatosan támadott, több ígéretes akciója is volt, de egyikért 
        sem kapott pontot. A földön hiába próbált fogást találni a grúzon, az nagyon 
        jól védekezett, s megőrizte előnyét az ötödik perc végéig. A cselgáncsozó 
        Ungvári Miklós reméli, hogy majd tud örülni az olimpiai ezüstéremnek, amelyben 
        úgy érzi, két évtized munkája fekszik. "Szeretném megköszönni az edzőmnek, 
        Bíró Tamásnak, akivel az elmúlt fél évben gyakorlatilag együtt éltünk, 
        azt a két évtizedes munkát, amelynek ez az olimpiai ezüstérem az eredménye" 
        - nyilatkozott az elvesztett vasárnapi döntő után a 66 kg-os dzsúdós. 
        "A családomnak, édesanyámnak, testvéreimnek is szeretném megköszönni, 
        akik kijöttek ide szurkolni nekem, s elnézést, ha a nap végére csalódást 
        okoztam, nem ezt akartam." A tatabányai versenyző a fináléról elmondta, 
        számított rá, hogy az elején a grúz Lasa Savdatuasvili azonnal támadóan 
        lép fel. "Az előző meccsekről tudtuk, hogy az utolsó másfél percre el fog 
        fáradni, de bevallom, nekem sem volt könnyű, én is elfáradtam" - mondta 
        a 31 éves Ungvári, hozzátéve, reméli, majd tud örülni az olimpiai 
        ezüstéremnek. "Hajtós Bertalan számára a mai napig fájó emlék az elvesztett 
        olimpiai döntő, még most is könnyes szemmel mesél róla. Nekem is az egész 
        életemre bennem lesz, hogy nem tudtam nyerni, pedig itt voltam a siker 
        kapujában, de nem tudtam belépni Kovács Antal mellé."</p>
    <p>Bíró Tamás szerint Ungvári fantasztikusan dolgozott egész nap. "Neki 2008-ban 
        kellett volna olimpiát nyernie, akkor volt a topon. Már nem egy fiatal 
        versenyzőről van szó, sokat kell fogyasztania, s összeakadt egy fiatal 
        csikóval, aki nagyon jó napot fogott ki. Nem vagyok elégedetlen, de Miklós 
        igen. Az elmúlt két olimpián nem volt szerencséje, most igen, nagyon sok 
        nagyágyú elhullott menet közben. Az elmúlt egy évben olyan lelkiállapotban 
        volt a testvére és magánéleti okok miatt, hogy nagyon nehéz volt felrázni." 
        Ungvári Miklós testvérét, a szintén válogatott cselgáncsozó Attilát 
        doppingvétség miatt eltiltották tavaly, a nemzetközi Sportdöntőbíróság (CAS) 
        a héten részben tisztázta a vádak alól. Ungvári olimpiai ezüstérmével 
        megkoronázta pályafutását, és - a szintén dzsúdós Csernoviczki Éva szombati 
        bronza után - a magyar küldöttség második érmét szerezte a XXX. nyári játékokon. 
        London előtt legutóbb magyar dzsúdós 20 éve, az 1992-es barcelonai játékokon 
        állhatott dobogóra, akkor egy arany-, két ezüst- és egy bronzérem volt a mérleg.</p>
