    <h3>Judo Kanji</h3>
    <img src="./img/kanji.jpg">
    <p>A fizikai és szellemi erő leghatékonyabb kifejlesztési módja az igazi 
    judo eljárás, melynek lényege a rendelkezésre álló energia leghatékonyabb 
    kihasználása a célkitűzés érdekében. A ju szócska jelentése alapján véve 
    „természetesnek lenni”, amely oly módú viselkedést kíván, amely szoros 
    összhangban áll a világegyetem igazságával, amelyet egész életünk során 
    követnünk kell. A ju szócska egyszer s mint azt is jelenti, hogy mindig 
    ésszerűen viselkedjünk, legyünk tisztességesek és kerüljük a hamisságot.” 
    Vallják a judo Japán kiválóságai.</p>
    <p>Ez a szellem, s az ősök tisztelete, a 
    tisztaság és becsületesség eszméje mélyen behatolt a Japán nép lelkületében, 
    s igen nagymértékben hozzájárult az értelmes, szorgalmas Japán nép 
    nemzeti vonásainak kialakulásához. A judo rendszere nemcsak a testi 
    ügyesség, hanem a szellemi fejlesztés céljait is követte. Fejlődésének 
    korai szakaszában még nem volt sem, kialakult tartalma sem pontosan 
    meghatározott neve. A régi krónikák ezeket, a harcművészeti eljárásokat, 
    mint harcművészeti mesterséget /kumiuchi = küzdelem, birkózás/ említik, 
    /lásd részletesen. JU JUTSU/, amely arra szolgál, hogy a rendszer 
    művelője a fogásrepertoár felhasználásával, egy kézitusában, puszta 
    kézzel is – a saját erejébe bízva – győzelmet tudjon aratni. Ezen cél 
    érdekében a judo /régebbi nevén yawara/ rendszer is a különböző harci 
    módozatokból átvette azok legcélravezetőbb módszereit, majd ezeket 
    tovább tökéletesítette.</p>
    <p>A Tokugawa korszak /1600-1868/ elején már 
    kb. 180 ilyen módszereket – nagyrészben titkosan – oktató iskolával 
    találkozhatunk, s ekkor kezdték ezen módszert jujutsu-nak 
    /lágy művészet/ nevezni. Ezekből a jól szervezett iskolákból 
    fejlődött ki a mai Kodokan judo, amelyet KANO JIGORO /1860-1938/ mester 
    a Meiji korszak /1868-1912/ 15. évében egyesített. Leginkább két stílust 
    tanulmányozott /tenshin shin’yo és kito stílus/, amelyek rendszerének 
    alapvetését adták. Első edzőtermét Tokio Shitaya városrészében az 
    Eisho-ji templomban rendezte be, ahol még csupán 12 szőnyegen /TATAMI/ 
    edzettek még részben a régi jujutsu módszer szerint. 
    A judo ezen kicsiny templomból indult világhódító útjára. 
    Kano – az általa gyakorolt jujutsu-t – az idők során bonckés alá veszi. 
    Számos – az életre, testi épségre – veszélyes fogást teljesen kiiktat a 
    rendszeréből. Bevezeti a szakszerű tompításos esést /ukemi/, mivel a 
    régi stílusokban nem ismerték a szabályszerű dobásból való esést, s 
    ebből sok sérülés és baleset adódott. Bevezette a szabályozott – szinte 
    sérülésmentes – küzdelmet /randori/. Evvel megteremtette a judo 
    versenysporttá válásának alapjait. 1911-től a fiú középiskolákban, 
    majd 1939-től az általános iskolák 5. osztályától kötelező tantárgy 
    lett a judo Japánban. Időközben Kano professzor igen nagy erőfeszítéssel 
    azon dolgozik, hogy a judo minél elterjedtebb művészetté, sporttá váljon. 
    És az sem volt titkolt vágya, hogy művészete az olimpiák rendszeres 
    résztvevője legyen. Ezen álma csak halála után az 1964-es Tokioi 
    Olimpiával teljesült be.</p>
    <p>A II. világháború után egy rövid szünet 
    következett /a megszálló amerikai hatóságok mindennemű harcművészet 
    gyakorlását betiltották Japán területén/, majd 1949-ben megalakul az 
    Össz Japán Judo Szövetség, 1951-ben pedig a nemzetközi Judo Szövetség. 
    1956-ban Tokióban megrendezték az első világbajnokságot, amely talán már 
    belépő volt az olimpiák világába. Az 1964-es olimpián bemutatkozik, majd 
    1972-től az olimpiák rendszeres sportága lesz a judo. Az olimpiai judo 
    versenyen tatamin /gyakorlószőnyeg/ küzdő pár közül az szerzi meg a 
    győzelmet, aki teljes értékű /ippon/ dobást kivitelezte vagy az ellenfél 
    karját úgy feszíti vagy nyakát úgy szorítja /fojtás/, hogy az a 
    mérkőzés – amely 3-20 percig terjedhet – feladni kényszerüljön, esetleg 
    az ellenfelet egy speciális fekvő pózban 30 mp-ig leszorítva tudja 
    tartani.</p>
    <p>Ma már 107 ország tartozik a nemzetközi szövetséghez. 
    /Hazánkban 1931-ben megalakul az első hivatalos judo klub, amelynek 
    vezetője – 1956-ig – Vincze Tibor. Az első Japán judo szakemberek pedig 
    csupán 1966-ban látogatnak először hazánkban./ Mára már nem csak Japánba, 
    hanem a világ sok általános és középiskolájában a testnevelés hivatalos 
    tantárgya a judo. Magyarországon először Pakson vezetik be a 
    judo-testnevelést. /Lásd még KANO JIGORO/.</p>
